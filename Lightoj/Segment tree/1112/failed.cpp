void make_tree(int node,int st, int ed)
{
    if (st==ed)
    {
        //cout<<"ara "<<ara[st]<<endl;
        tree[node]=ara[st];
        return;
    }
    int left=node*2;
    int right=node*2+1;
    int mid=(st+ed)/2;
    make_tree(left,st,mid);
    make_tree(right,mid+1,ed);
    tree[node]=tree[left]+tree[right];
    //cout<<"ZY"<<endl;
}

void update_tree(int node,int st, int ed,int pos, int val)
{
    if (st>pos || ed<pos) return;
    if (st>=pos && ed<=pos)
    {
        tree[node]=val;
        //cout<<"tree "<<tree[node]<<endl;
        return;
    }
    int left=node*2;
    int right=node*2+1;
    int mid=(st+ed)/2;
    update_tree(left,st,mid,pos,val);
    update_tree(right,mid+1,ed,pos,val);
    tree[node]=tree[left]+tree[right];
}

int calculate_tree(int node,int st, int ed,int a, int b)
{
    if (st>b || ed<a) return 0;
    if (st>=a && ed<=b)
    {
        return tree[node];
    }
    int left=node*2;
    int right=node*2+1;
    int mid=(st+ed)/2;
    return (calculate_tree(left,st,mid,a,b))+(calculate_tree(right,mid+1,ed,a,b));
}

