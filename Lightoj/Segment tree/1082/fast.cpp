void update_tree (int node, int r,int e,int ara[])
{
    //cout<<"Y"<<endl;
    if (r==e)
    {
        //cout<<"node "<<node<<" update "<<ara[r]<<endl;
        tree[node]=ara[r];
        return;
    }
    int left=node<<1;
    int right=left+1;
    int mid=(r+e)>>1;
    update_tree(left,r,mid,ara);
    update_tree(right,mid+1,e,ara);
    if (tree[left]<tree[right])
    {
        tree[node]=tree[left];
    }
    else
    {
        tree[node]=tree[right];
    }
    //cout<<"Y"<<endl;
}
int calculate_min (int node, int r, int e, int a , int b)
{
    //cout<<"y"<<endl;
    if (a>e || b<r) return 100005;
    if (r>=a && e<=b)
    {
            return tree[node];

    }
    int left=2*node;
    int right=(2*node)+1;
    int mid=(r+e)/2;
    if (a>mid)
        {
             return calculate_min(right,mid+1,e,a,b);
        }
    else if (b<=mid)
    {
        calculate_min(left,r,mid,a,b);
    }
    else
        return  min(calculate_min(left,r,mid,a,b),calculate_min(right,mid+1,e,a,b));
}
